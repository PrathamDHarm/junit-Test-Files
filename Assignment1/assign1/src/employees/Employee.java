package employees;

public class Employee {
	String name;
	int age,salary;
	
	public String getName() {
		return name;
	}
	public String setName(String name) {
		return this.name = name;
	}
	public int getAge() {
		return age;
	}
	public int setAge(int age) {
		return this.age = age;
	}
	public int getSalary() {
		return salary;
	}
	public int setSalary(int salary) {
		return this.salary = salary;
	}
	
}
